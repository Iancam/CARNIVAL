CP_NickNames <- c("APAP","CCl4","CYCA","DFN","PPNL")
Conditions <- list()
Conditions[[1]] <- c("low_2h","high_24h")
Conditions[[2]] <- c("low_8h","high_2h")
Conditions[[3]] <- c("low_8h","high_24h")
